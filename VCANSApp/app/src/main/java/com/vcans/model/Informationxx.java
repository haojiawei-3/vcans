package com.vcans.model;

public class Informationxx {
	public String strXxId="";
	public String strCzId="";
	public String strItem="";
	public String strCzname="";
	
	
	public Informationxx() {
	}
	
	
	public Informationxx(String strXxId, String strCzId, String strItem,
			String strCzname) {
		super();
		this.strXxId = strXxId;
		this.strCzId = strCzId;
		this.strItem = strItem;
		this.strCzname = strCzname;
	}
	public String getStrXxId() {
		return strXxId;
	}
	public void setStrXxId(String strXxId) {
		this.strXxId = strXxId;
	}
	public String getStrCzId() {
		return strCzId;
	}
	public void setStrCzId(String strCzId) {
		this.strCzId = strCzId;
	}
	public String getStrItem() {
		return strItem;
	}
	public void setStrItem(String strItem) {
		this.strItem = strItem;
	}
	public String getStrCzname() {
		return strCzname;
	}
	public void setStrCzname(String strCzname) {
		this.strCzname = strCzname;
	}
	
}
